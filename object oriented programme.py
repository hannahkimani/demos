class People:
    def __init__(self,name, age, nationality):
        self.name = name
        self.age = age
        self.nationality = nationality

person1 = People("Hannah", 19, "Norwegian")

print(person1.name, person1.age, person1.nationality)
print(f"Hi, my name is {person1.name} and I am {person1.age} years old and I am a {person1.nationality}")















